		<p class="leading-relaxed mb-6">
			Who is God to you? What do you know about Him? Perhaps some of you are like me; dragged by
			their parents to church on Sundays. At a young age I knew of God based on what I learned at
			Sunday school. I know that God created the world, that He created man and woman, He opened up
			the Red sea, and saved Daniel from the lion's den. We know all these Bible stories.
		</p>

		<p class="text-xl font-semibold mb-6 text-center">But who is God?</p>

		<p class="leading-relaxed mb-6">
			Jesus said that He is the only one who fully knows God; the only One who accurately
			understands the Father (Matthew 11:27). God has always been a mystery to me. God's "invisible
			qualities - his eternal power and divine nature - have been clearly seen (Romans 1:20)" in His
			creation, yet I still cannot seem to have a full grasp of who God is. Surely, I cannot see
			God, yet I feel so loved by Him. I cannot see God, yet He gives me peace that transcends all
			things, hope that never disappoints, provisions that overflow and love that satisfies. We can
			all learn something new about God every single day.
		</p>

