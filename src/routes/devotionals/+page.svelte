		<p class="leading-relaxed mb-6">
			When I get asked questions such as, How do I stop my porn addiction? How do I stop engaging in
			gossip? How do I stop judging others? How do I stop cursing? How can I forgive when it's hard?
			My answer to these questions is simple. Fall in love with God.
		</p>

		<p class="leading-relaxed mb-6">
			When you pour your energy into stopping yourself from sinning, you are still trying to rid
			yourself of the sin/addiction by your own strength. Instead, pour your energy into reading
			your bible, praying daily, and fellowshipping with other followers of Christ. By doing this,
			you are allowing the Holy Spirit to work in you. The more you do this daily and consistently,
			the more you will fall in love with God, and the more encounters you will have with Him. God
			CAN deliver and restore you, even if you are the worst of sinners (2 Chronicles 7:14).
		</p>

		<p class="leading-relaxed mb-8">
			So I encourage you today to draw near to God. Call unto the Lord, and let Him reveal His
			wonderful mysteries to you.
