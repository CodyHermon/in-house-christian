	Fall In Love With God (Written by: Kahlil Hermon) ​ Call to me and I will answer you, and will
	tell you great and hidden things that you have not known. Jeremiah 33:3 (ESV) ​ Who is God to you?
	What do you know about Him? Perhaps some of you are like me; dragged by their parents to church on
	Sundays. At a young age I knew of God based on what I learned at Sunday school. I know that God
	created the world, that He created man and woman, He opened up the Red sea, and saved Daniel from
	the lion’s den. We know all these Bible stories. But who is God? Jesus said that He is the only
	one who fully knows God; the only One who accurately understands the Father (Matthew 11:27). God
	has always been a mystery to me. God’s “invisible qualities - his eternal power and divine nature
	- have been clearly seen (Romans 1:20)” in His creation, yet I still cannot seem to have a full
	grasp of who God is. Surely, I cannot see God, yet I feel so loved by Him. I cannot see God, yet
	He gives me peace that transcends all things, hope that never disappoints, provisions that
	overflow and love that satisfies. We can all learn something new about God every single day. When
	I get asked questions such as, How do I stop my porn addiction? How do I stop engaging in gossip?
	How do I stop judging others? How do I stop cursing? How can I forgive when it’s hard? My answer
	to these questions is simple. Fall in love with God. When you pour your energy into stopping
	yourself from sinning, you are still trying to rid yourself of the sin/addiction by your own
	strength. Instead, pour your energy into reading your bible, praying daily, and fellowshipping
	with other followers of Christ. By doing this, you are allowing the Holy Spirit to work in you.
	The more you do this daily and consistently, the more you will fall in love with God, and the more
	encounters you will have with Him. God CAN deliver and restore you, even if you are the worst of
	sinners (2 Chronicles 7:14). So I encourage you today to draw near to God. Call unto the Lord, and
	let Him reveal His wonderful mysteries to you.
